package CoreJavaBrushUp;

public class MethodDemo1 {

    // Non-static method getData()
    public String getData() {
        System.out.println("Hello World");  // Prints a message to the console
        return "Adhirth Dalwai";  // Returns a string value
    }

    // Static method getData2()
    public static String getData2() {
        System.out.println("Hello World 2");  // Prints another message to the console
        return "Adhirth Dalwai";  // Returns a string value
    }
}
